# Funções para operações básicas - Atividade03
# função para somar
def somar(a, b):
  soma = a + b
  return soma


# função para subtrair
def subtrair(a, b):
  subtracao = a - b
  return subtracao


# função para multiplicar
def multiplicar(a, b):
  multiplicacao = a * b
  return multiplicacao


# função para dividir
def dividir(a, b):
  divisao = a / b
  return divisao
